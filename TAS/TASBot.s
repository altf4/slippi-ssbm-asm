# Allstar Mode
.set CMD_REQ_ALLSTAR_ORDER, 0x74
    .set REQ_ALLSTAR_CMD_OFFSET, 0
        .set REQ_ALLSTAR_CMD_SIZE, 1
    .set REQ_ALLSTAR_CKIND_OFFSET, REQ_ALLSTAR_CMD_OFFSET + REQ_ALLSTAR_CMD_SIZE
        .set REQ_ALLSTAR_CKIND_SIZE, 1
    .set REQ_ALLSTAR_COSTUME_OFFSET, REQ_ALLSTAR_CKIND_OFFSET + REQ_ALLSTAR_CKIND_SIZE
        .set REQ_ALLSTAR_COSTUME_SIZE, 1
    .set REQ_ALLSTAR_DIFFICULTY_OFFSET, REQ_ALLSTAR_COSTUME_OFFSET + REQ_ALLSTAR_COSTUME_SIZE
        .set REQ_ALLSTAR_DIFFICULTY_SIZE, 1
    .set REQ_ALLSTAR_BUFSIZE, REQ_ALLSTAR_DIFFICULTY_OFFSET + REQ_ALLSTAR_DIFFICULTY_SIZE

    .set GET_ALLSTAR_STAGE_OFFSET, 0
        .set GET_ALLSTAR_STAGE_SIZE, 1
    .set GET_ALLSTAR_CKIND_OFFSET, GET_ALLSTAR_STAGE_OFFSET + GET_ALLSTAR_STAGE_SIZE
        .set GET_ALLSTAR_CKIND_SIZE, 1
    .set GET_ALLSTAR_BUFSIZE, (GET_ALLSTAR_CKIND_OFFSET + GET_ALLSTAR_CKIND_SIZE) * 24


.set CMD_REQ_ALLSTAR_RNG, 0x75
    .set REQ_ALLSTAR_RNG_CMD_OFFSET, 0
        .set REQ_ALLSTAR_RNG_CMD_SIZE, 1
    .set REQ_ALLSTAR_RNG_INDEX_OFFSET, REQ_ALLSTAR_RNG_CMD_OFFSET + REQ_ALLSTAR_RNG_CMD_SIZE
        .set REQ_ALLSTAR_RNG_INDEX_SIZE, 1
    .set REQ_ALLSTAR_RNG_BUFSIZE, REQ_ALLSTAR_RNG_INDEX_OFFSET + REQ_ALLSTAR_RNG_INDEX_SIZE

    .set GET_ALLSTAR_RNG_OFFSET, 0
        .set GET_ALLSTAR_RNG_SIZE, 4
    .set GET_ALLSTAR_RNG_BUFSIZE, GET_ALLSTAR_RNG_OFFSET + GET_ALLSTAR_RNG_SIZE


# Adventure Mode
.set CMD_REQ_ADV_DATA, 0x76
    .set REQ_ADV_CMD_OFFSET, 0
        .set REQ_ALLSTAR_CMD_SIZE, 1
    .set REQ_ADV_CKIND_OFFSET, REQ_ADV_CMD_OFFSET + REQ_ALLSTAR_CMD_SIZE
        .set REQ_ALLSTAR_CKIND_SIZE, 1
    .set REQ_ADV_COSTUME_OFFSET, REQ_ADV_CKIND_OFFSET + REQ_ALLSTAR_CKIND_SIZE
        .set REQ_ALLSTAR_COSTUME_SIZE, 1
    .set REQ_ADV_DIFFICULTY_OFFSET, REQ_ADV_COSTUME_OFFSET + REQ_ALLSTAR_COSTUME_SIZE
        .set REQ_ALLSTAR_DIFFICULTY_SIZE, 1
    .set REQ_ADV_STOCK_OFFSET, REQ_ADV_COSTUME_OFFSET + REQ_ALLSTAR_COSTUME_SIZE
        .set REQ_ALLSTAR_STOCK_SIZE, 1
    .set REQ_ADV_BUFSIZE, REQ_ADV_STOCK_OFFSET + REQ_ALLSTAR_STOCK_SIZE


.set CMD_REQ_ADV_RNG, 0x77
    .set REQ_ADV_RNG_CMD_OFFSET, 0
        .set REQ_ADV_RNG_CMD_SIZE, 1
    .set REQ_ADV_RNG_INDEX_OFFSET, REQ_ADV_RNG_CMD_OFFSET + REQ_ADV_RNG_CMD_SIZE
        .set REQ_ADV_RNG_INDEX_SIZE, 1
    .set REQ_ADV_RNG_BUFSIZE, REQ_ADV_RNG_INDEX_OFFSET + REQ_ADV_RNG_INDEX_SIZE

    .set GET_ADV_RNG_OFFSET, 0
        .set GET_ADV_RNG_SIZE, 4
    .set GET_ADV_RNG_BUFSIZE, GET_ADV_RNG_OFFSET + GET_ADV_RNG_SIZE
